public class SimpleMath {

	public static void main (String[] args) {
		
		double length = 2.3; 				//The length of the box.
		double width = 3.1; 				//The width of the box.
		int height = 2;					//The height of the box.
		double volume = length * width * height; 	//The volume of the box.
		System.out.println("The volume of my box with sides " + length + ", " + width + ", " + height + " and box is " + volume + " cubic feet.");
	}
}